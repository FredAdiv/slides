val = 42

print(f"{val:b}") #  binary:    101010
print(f"{val:c}") #  character: *
print(f"{val:d}") #  decimal:   42      (default)
print(f"{val:o}") #  octal:     52
print(f"{val:x}") #  hexa:      2a
print(f"{val:X}") #  hexa:      2A
print(f"{val:n}") #  number:    42


print(f"{val}")   # 42 (same as decimal)
